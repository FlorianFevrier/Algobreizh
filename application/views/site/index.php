    <h1>Algobreizh</h1>
    <p><span class="image left"><img src="http://localhost/Algobreizh/images/AlgoBreizh_Logo_512px_mono.png" alt="Algobreizh"/></span>
AlgoBreizh est basée à Lampaul-Plouarzel face à la Mer d'Iroise, l'un des plus grands champs 
d'algues d'Europe. Les produits que nous fabriquons sont vendus sous la marque «
Plein Ouest
»
Contrairement à ce que l'on peut observer en Asie, en Europe le marché de l'algue alimentaire 
est un marché de niche. Les progressions de ces dernières années montrent que l'algue gagne du 
terrain tant auprès des restaurateurs que des particuliers.
Depuis plus de 15 ans, toute l'équipe d'AlgoBreizh développe et fabrique une gamme originale de
produits alimentaires à base d'algues pour le bonheur des chefs et des particuliers amateurs de 
nouvelles saveurs. AlgoBreizh est une coopérative regroupant 15 producteurs d'algues et assurant une valorisation 
de leur production sous la forme de produits alimentaires à forte valeur ajoutée, ainsi que leur 
commercialisation auprès de la grande distribution et de commerces spécialisés.
L'unité de transformation du Cleguer emploie 18 salariés, tandis que la commercialisation, au 
siège, concerne 15 personnes, dont 11 commerciaux visitant les distributeurs et magasins 
clients.
Les chiffres économiques les plus récents sont ceux relatifs à l'exercice 2013, clôturé le 31 
décembre 2013.
